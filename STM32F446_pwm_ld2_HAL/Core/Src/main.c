/**
  ******************************************************************************
  * @file           : main.c
  * @brief          : Main program body
  ******************************************************************************
  * This project shows how to use Timer 2 (TIM2) to generate a PWM-signal on
  * User Led LD2 (pin PA5). The core runs at 180 MHz and TIM2 runs at 90 MHz.
  * The prescaler is loaded with 0 (so set to 1) and the timer value runs from
  * 0 to 9999 (inclusive, so 10000 steps). This will generate a PWM frequency
  * of 9 kHz.
  *
  * The Duty Cycle (DC) is calculated every 1 ms and will run linear from
  * 0 to 9999 in steps of 10. There is an option to compensate the DC for the
  * perception of the human eye, because light intensity is not linear to the
  * human eye. Leave all values to the default values for a first inspection.
  *
  * We will:
  * write directly to Compare Register CH1 to avoid stopping the PWM
  * generation. Using the HAL functions, the PWM generation will stop
  * for a short time and then starts again. We don't want that, so we
  * update the compare register directly. See:
  * https://community.st.com/s/question/0D50X00009XkaM3SAJ/changing-of-pwm-duty-cycle-in-an-efficient-manner
  *
  * Please note that the perception of the human eye is non-linear and
  * is modeled with the Weber–Fechner law. See:
  * https://en.wikipedia.org/wiki/Weber%E2%80%93Fechner_law
  * So in real life the CCR1 register should be loaded by a corrected
  * value. The formula below compensates for the human eye perception.
  * Now the brightness of a led will almost appear linear to the human
  * eye.
  *
  ******************************************************************************
  */
